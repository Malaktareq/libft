#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	a;
	int	b;

	a = 0;
	b = 1;
	while (*nptr == 32 || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '+' || *nptr == '-')
		if (*nptr++ == '-')
			b *= -1;
	while (*nptr >= '0' && *nptr <= '9')
		a = a * 10 + (*nptr++ - '0');
	return (a * b);
}
